/*1.Swapping value of Two variables?*/
package Task1;

public class SwappingTwoNumbers {
	public static void main(String[] args) {
		int temp;
		int x = 100;
		int y = 200;
		//Swapping in Steps//
		temp = x;
		x = y;
		y = temp;
		//Verify swapped values
		System.out.println("x = "+x+ " and y "+y);
		
	}

}
